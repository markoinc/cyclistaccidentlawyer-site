// Helper functions
export function getStateBySlug(slug: string): State | undefined {
  return states.find(s => s.slug === slug);
}

export function getCityBySlug(stateSlug: string, citySlug: string): City | undefined {
  const state = getStateBySlug(stateSlug);
  return state?.cities.find(c => c.slug === citySlug);
}

export function getAllCities(): City[] {
  return states.flatMap(s => s.cities);
}

export function getHelmetRequiredStates(): State[] {
  return states.filter(s => s.helmetAge !== "None");
}

export function getNoHelmetLawStates(): State[] {
  return states.filter(s => s.helmetLaw.includes("No statewide"));
}

export function getContributoryNegligenceStates(): State[] {
  return states.filter(s => s.faultSystem === "Contributory negligence");
}

export function getVulnerableRoadUserLawStates(): State[] {
  return states.filter(s => s.vulnerableRoadUserLaw);
}

export function getIdahoStopStates(): State[] {
  return states.filter(s => 
    s.bikeLaneRights.toLowerCase().includes("idaho stop") || 
    s.bikeLaneRights.toLowerCase().includes("safety stop") ||
    s.bikeLaneRights.toLowerCase().includes("treat stop")
  );
}

export function getShortSOLStates(): State[] {
  return states.filter(s => s.statOfLimitations === "1 year");
}

export function getHighFatalityStates(): State[] {
  return states.filter(s => s.annualCyclistFatalities >= 30);
}
